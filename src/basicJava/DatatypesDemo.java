package basicJava;

public class DatatypesDemo {

	public static void main(String[] args) {

		//Boolean true/false- 1 bit
		
		boolean status=true;
		System.out.println("Boolean data is: "+status);
		status=false;
		System.out.println("New Updated status is: "+status);
		
		boolean new_status=true;
		System.out.println("New status is:"+new_status);
		
		System.out.println("****************************");
		
		//char
		char c1='A';
		System.out.println("Character data is:"+c1);
		char c2='@';
		System.out.println("Special character is:"+c2);
		char c3=90;
		System.out.println("Ascii character is:"+c3);
		
		char c4=99;
		System.out.println("Character for ascii:"+c4);
		
		char c5=49;
		System.out.println("Character for ascii 49 is:"+c5);
		
		System.out.println("***************************");
		
		//byte - 1byte
		byte b1=125;
		System.out.println("Byte data is:"+b1);
		byte b2=-127;
		System.out.println("Byte data is:"+b2);
		
		//byte b3=-130;
		//byte b4=150;
		
		System.out.println("*************************");
		
		//short- 2 byte
		short s1=15000;
		System.out.println("short data is:"+s1);
		short s2=-32768;
		System.out.println("short data is:"+s2);
		short s3=32767;
		System.out.println("short data is "+s3);
		//short s4=45000;
		
		System.out.println("*************************");
		
		//integer
		int i1=55000;
		System.out.println("integer data is:"+i1);
		int i2=7865435;
		System.out.println("integer data is:"+i2);
		//int i3=999999999999999999999;
		
		System.out.println("*************************");
		
		//long
		long l1=5566778l;
		System.out.println("long data is:"+l1);
		long l2=6789l;
		System.out.println("long data is:"+l2);
		
		System.out.println("*************************");
		
		//float
		float f1=56.78f;
		System.out.println("float data is:"+f1);
		float f2=675.765f;
		System.out.println("float data is:"+f2);
		
		System.out.println("*************************");
		
		//double
		double d1=678908.6543;
		System.out.println("double data is:"+d1);
		double d2=65.67;
		System.out.println("double data is:"+d2);
		
		
	}

}
